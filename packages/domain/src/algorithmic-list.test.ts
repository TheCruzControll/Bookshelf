describe("findQueryDefinition", () => {
  it("returns the definition for a known type", () => {
    const result = findQueryDefinition("trending_in_circle");
    expect(result).toBeDefined();
    expect(result!.type).toBe("trending_in_circle");
    expect(result!.title).toBe("Trending in Your Circle");
  });

  it("returns undefined for an unknown type", () => {
    const result = findQueryDefinition("nonexistent" as AlgorithmicQueryType);
    expect(result).toBeUndefined();
  });

  it("returns each registered definition", () => {
    for (const def of ALGORITHMIC_QUERY_REGISTRY) {
      const found = findQueryDefinition(def.type);
      expect(found).toBe(def);
    }
  });
});

