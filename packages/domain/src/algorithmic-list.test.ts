describe("getQueriesByScope", () => {
  it("returns only per_user queries when filtering by per_user", () => {
    const perUser = getQueriesByScope("per_user");
    expect(perUser.length).toBe(3);
    for (const def of perUser) {
      expect(def.scope).toBe("per_user");
    }
  });

  it("returns only global queries when filtering by global", () => {
    const global = getQueriesByScope("global");
    expect(global.length).toBe(2);
    for (const def of global) {
      expect(def.scope).toBe("global");
    }
  });

  it("per_user + global covers all registered queries", () => {
    const perUser = getQueriesByScope("per_user");
    const global = getQueriesByScope("global");
    expect(perUser.length + global.length).toBe(ALGORITHMIC_QUERY_REGISTRY.length);
  });
});

