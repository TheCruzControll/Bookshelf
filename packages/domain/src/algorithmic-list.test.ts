describe("ALGORITHMIC_QUERY_REGISTRY", () => {
  it("contains exactly 5 launch queries", () => {
    expect(ALGORITHMIC_QUERY_REGISTRY).toHaveLength(5);
  });

  it("has unique query types", () => {
    const types = ALGORITHMIC_QUERY_REGISTRY.map((q) => q.type);
    expect(new Set(types).size).toBe(types.length);
  });

  it("includes the 5 expected query types", () => {
    const types = ALGORITHMIC_QUERY_REGISTRY.map((q) => q.type);
    expect(types).toContain("trending_in_circle");
    expect(types).toContain("new_on_hone");
    expect(types).toContain("highly_ranked_this_month");
    expect(types).toContain("popular_in_top_genre");
    expect(types).toContain("mutuals_reading_now");
  });

  it("all definitions have non-empty title and description", () => {
    for (const def of ALGORITHMIC_QUERY_REGISTRY) {
      expect(def.title.length).toBeGreaterThan(0);
      expect(def.description.length).toBeGreaterThan(0);
    }
  });

  it("all definitions have valid default params", () => {
    for (const def of ALGORITHMIC_QUERY_REGISTRY) {
      expect(def.defaultParams.maxItems).toBeGreaterThan(0);
      expect(def.defaultParams.maxItems).toBeLessThanOrEqual(100);
      expect(def.defaultParams.windowDays).toBeGreaterThanOrEqual(0);
      expect(def.defaultParams.minThreshold).toBeGreaterThanOrEqual(0);
    }
  });
});

