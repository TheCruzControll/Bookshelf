describe("resolveParams", () => {
  const definition: AlgorithmicQueryDefinition = {
    type: "new_on_hone",
    title: "New on Hone",
    description: "test",
    defaultParams: { userId: "", windowDays: 14, maxItems: 20, minThreshold: 3 },
    scope: "global",
  };

  it("returns defaults when no overrides provided", () => {
    const result = resolveParams(definition);
    expect(result).toEqual(definition.defaultParams);
  });

  it("returns defaults when overrides is undefined", () => {
    const result = resolveParams(definition, undefined);
    expect(result).toEqual(definition.defaultParams);
  });

  it("merges overrides with defaults", () => {
    const result = resolveParams(definition, { maxItems: 50 });
    expect(result.maxItems).toBe(50);
    expect(result.windowDays).toBe(14); // kept default
    expect(result.minThreshold).toBe(3); // kept default
  });

  it("overrides userId for per-user queries", () => {
    const result = resolveParams(definition, { userId: "user-123" });
    expect(result.userId).toBe("user-123");
  });

  it("overrides all params when all are provided", () => {
    const overrides: QueryParams = {
      userId: "user-xyz",
      windowDays: 30,
      maxItems: 10,
      minThreshold: 5,
    };
    const result = resolveParams(definition, overrides);
    expect(result).toEqual(overrides);
  });
});
