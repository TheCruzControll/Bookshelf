describe("validateQueryParams", () => {
  it("returns empty array for valid params", () => {
    const errors = validateQueryParams({ windowDays: 7, maxItems: 20, minThreshold: 2 });
    expect(errors).toHaveLength(0);
  });

  it("returns empty array for empty params (all optional)", () => {
    const errors = validateQueryParams({});
    expect(errors).toHaveLength(0);
  });

  it("reports error for negative windowDays", () => {
    const errors = validateQueryParams({ windowDays: -1 });
    expect(errors).toContain("windowDays must be non-negative");
  });

  it("reports error for maxItems below 1", () => {
    const errors = validateQueryParams({ maxItems: 0 });
    expect(errors).toContain("maxItems must be between 1 and 100");
  });

  it("reports error for maxItems above 100", () => {
    const errors = validateQueryParams({ maxItems: 101 });
    expect(errors).toContain("maxItems must be between 1 and 100");
  });

  it("reports error for negative minThreshold", () => {
    const errors = validateQueryParams({ minThreshold: -1 });
    expect(errors).toContain("minThreshold must be non-negative");
  });

  it("reports multiple errors simultaneously", () => {
    const errors = validateQueryParams({ windowDays: -1, maxItems: 0, minThreshold: -5 });
    expect(errors).toHaveLength(3);
  });

  it("accepts boundary values (windowDays=0, maxItems=1, maxItems=100)", () => {
    expect(validateQueryParams({ windowDays: 0 })).toHaveLength(0);
    expect(validateQueryParams({ maxItems: 1 })).toHaveLength(0);
    expect(validateQueryParams({ maxItems: 100 })).toHaveLength(0);
    expect(validateQueryParams({ minThreshold: 0 })).toHaveLength(0);
  });
});

